/**
 * All possible equipment slots occupied by an item. An item can occupy multiple
 * slots, e.g. a two-handed weapon will occupy both MainHand and OffHand.
 */
export enum EquipmentSlot {
    MainHand,
    OffHand,
    Head,
    Chest,
    Hands,
    Belt,
    Legs,
    Feet,
    Fingers,
    Neck,
}
